#include "cta/common/Exception.hpp"

//------------------------------------------------------------------------------
// constructor
//------------------------------------------------------------------------------
cta::common::Exception::Exception(const std::string &message):
  m_message(message) {
}

//------------------------------------------------------------------------------
// destructor
//------------------------------------------------------------------------------
cta::common::Exception::~Exception() throw() {
}

//------------------------------------------------------------------------------
// what
//------------------------------------------------------------------------------
const char *cta::common::Exception::what() const throw() {
  return m_message.c_str();
}
