#include "cta/Mount.hpp"

//------------------------------------------------------------------------------
// constructor
//------------------------------------------------------------------------------
cta::Mount::Mount(const std::string &id, const std::string &vid):
  m_id(id),
  m_vid(vid) {
}

//------------------------------------------------------------------------------
// destructor
//------------------------------------------------------------------------------
cta::Mount::~Mount() throw() {
}

//------------------------------------------------------------------------------
// getId
//------------------------------------------------------------------------------
const std::string &cta::Mount::getId() const throw() {
  return m_id;
}

//------------------------------------------------------------------------------
// getVid
//------------------------------------------------------------------------------
const std::string &cta::Mount::getVid() const throw() {
  return m_vid;
}
