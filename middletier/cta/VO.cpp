#include "cta/VO.hpp"

//------------------------------------------------------------------------------
// constructor
//------------------------------------------------------------------------------
cta::VO::VO() {
}

//------------------------------------------------------------------------------
// destructor
//------------------------------------------------------------------------------
cta::VO::~VO() throw() {
}

//------------------------------------------------------------------------------
// constructor
//------------------------------------------------------------------------------
cta::VO::VO(
  const std::string &name,
  const UserIdentity &creator,
  const std::string &comment,
  const time_t creationTime):
  ConfigurationItem(creator, comment, creationTime),
  m_name(name) {
}

//------------------------------------------------------------------------------
// getName
//------------------------------------------------------------------------------
const std::string &cta::VO::getName() const throw() {
  return m_name;
}
